const { Router } = require('express');
const { check } = require('express-validator');
const { validarJWT } = require('../middlewares/validar-jwt');

const {
        getDocumentosStatus,
        getDocumentoId,
        getDocumentos,
        postDocumento,
        putDocumentoResultado,
        putDocumentoValidacion,
} = require('../controllers/doc.controllers');

const router = Router();

////////////////////////////////////////////////////////////////////////////
//regresa a todos los Documentos
////////////////////////////////////////////////////////////////////////////
router.get('/',[
                validarJWT
],
 getDocumentos);

////////////////////////////////////////////////////////////////////////////
//regresa doc por id
////////////////////////////////////////////////////////////////////////////
router.get('/:id', getDocumentoId);

////////////////////////////////////////////////////////////////////////////
//regresa a todos los documentos por su estatus
////////////////////////////////////////////////////////////////////////////
router.get('/estatus/:status', getDocumentosStatus);


///////////////////////////////////////////////////////////////////////////
//registrar datos de un dosumento
////////////////////////////////////////////////////////////////////////////
router.post('/',
        [
                validarJWT,
        ],
        postDocumento);

///////////////////////////////////////////////////////////////////////////
//actualizar resultados del documento
////////////////////////////////////////////////////////////////////////////
router.put('/:id',
        [
                validarJWT,
        ],
        putDocumentoResultado);

///////////////////////////////////////////////////////////////////////////
//actualizar resultados del documento
////////////////////////////////////////////////////////////////////////////
router.put('/valid/:id',
        [
                validarJWT,
        ],
        putDocumentoValidacion);








module.exports = router;

